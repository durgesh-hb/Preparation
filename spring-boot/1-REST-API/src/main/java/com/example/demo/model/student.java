package com.example.demo.model;

public class student {

    private int id;
    private String name;
    private int sem;

    public int getid(){
        return id;
    }

    public void setid(int id){
        this.id=id;
    }

    public String getname(){
        return name;
    }

    public void setname(String name){
        this.name=name;
    }

    public int getsem(){
        return sem;
    }

    public void setsem(int sem){
        this.sem=sem;
    }
}
